#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - function to add
 * @a: first parameter
 * @b: second value
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to substraction
 * @a: first parameter
 * @b: second value
 *
 * Return: a * b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function to multiply
 * @a: first parameter
 * @b: second value
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function to modulus
 * @a: first parameter
 * @b: second value
 *
 * Return: a % b
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to add
 * @a: first parameter
 * @b: second value
 *
 * Return: a + b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
